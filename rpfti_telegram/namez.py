def make_family_name(word, gender):
    word = word.strip()
    endings = None
    if gender == genders.MALE:
        endings = endings_m
    else:
        endings = endings_f
    for e in endings:
        if word[-1] in e[0]:
            return word + random.choice(e[1])


def get_random_name(gender=genders.RANDOM, words=[]):
    if gender in [genders.RANDOM, genders.INDEF, None]:
        gender = random.choice([genders.FEMALE, genders.MALE])
    if gender == genders.FEMALE:
        names = names_f
    else:
        names = names_m
    name = random.choice(names)
    fname = make_family_name(random.choice(names_m + names_f + words), gender)
    return gender, "{} {}".format(name, fname).title()
